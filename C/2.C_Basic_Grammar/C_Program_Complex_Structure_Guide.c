#include <stdio.h>   // 头文件包含

#define PI 3.14159    // 宏定义

// 函数声明

/*在 C 语言中，函数的声明必须在函数定义或调用之前。
声明提供了函数的返回类型、函数名和参数列表，
以便编译器知道如何调用该函数。*/
int add(int a, int b);

int main() {         // 主函数
    
    // 变量声明
    
    /*在 C 程序中，所有变量必须在使用前声明。
    变量可以在 main() 函数中声明，也可以在其他函数中或全局声明。*/
    int num1, num2, sum;
    // 提示用户输入
    /*语句和表达式：
    语句是 C 程序的基本执行单元，
    通常是函数调用、赋值、控制语句（如 if、for 等）或表达式。
    表达式是由变量、操作符和常量组成的代码片段。*/
    printf("Enter two integers: ");
    // 使用scanf函数读取用户输入的两个整数，并分别存储在num1和num2变量中
    // %d是格式说明符，表示要读取一个整数
    // &num1和&num2是变量num1和num2的地址，scanf函数会将输入的值存储到这些地址指向的变量中
    scanf("%d %d", &num1, &num2);

    // 函数调用
    sum = add(num1, num2);

    // 输出结果
    printf("Sum: %d\n", sum);

    return 0;     
    //返回语句return 语句用于终止函数的执行，并将控制权交还给调用函数。
    //main() 函数的返回值通常是 0 表示正常执行。
}

// 函数定义
//函数定义包含实际的函数体，它描述了函数的具体实现。函数通常包含参数、局部变量和返回值。
int add(int a, int b) {
    return a + b;

}

